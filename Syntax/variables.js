// let
console.log('~~~~~ let ~~~~~');

let price;
console.log(price);
price = 10.00;
console.log(price);
price = 40.00;
console.log(price);

// const
console.log('~~~~~ const ~~~~~');

// const must be assigned a value and cannot be changed
const constantValue = 10;
